<template>
	<div class="ratingselect">
		<div class="rating-type border-1px">
			<span class="block posi" :class="{'active':selectType===2}" @click="select(2,$event)">{{desc.all}}<span class="count">{{ratings.length}}</span></span>
			<span class="block posi" :class="{'active':selectType===0}" @click="select(0,$event)">{{desc.positive}}<span class="count">{{positives.length}}</span></span>
			<span class="block nega" :class="{'active':selectType===1}" @click="select(1,$event)">{{desc.negative}}<span class="count">{{negatives.length}}</span></span>		
		</div>
		<div class="switch" :class="{'on': onlyContent}">
			<span class="icon-check_circle" @click="toggleContent"></span>
			<span class="text">只要有内容的评价</span>
		</div>
	</div>
</template>

<script type="text/javascript">

 	const POSITIVE = 0;
 	const NEGATIVE = 1;
 	const ALL = 2;

	export default{
		data(){
			return{
			}
		},
		props:{
			ratings:{
				type: Array,
				default(){
					return []
				}
			},
			selectType:{
				type: Number,
				default: ALL
			},
			onlyContent:{
				type: Boolean,
				default: false
			},
			desc:{
				type: Object,
				default(){
					return{
						all: '全部',
						positive: '满意',
						negative: '不满意'
					};
				}
			}
		},
		created(){
		},
		methods:{
			select(type,event){
				if(!event._constructed){
					return;
				};
				this.$emit('changeType', type );
			},
			toggleContent(){
				if(!event._constructed){
					return;
				};
				this.$emit('changeContent');
			}
		},
		computed:{
			positives(){
				return this.ratings.filter((rating)=>{
					return rating.rateType === POSITIVE;
				});
			},
			negatives(){
				return this.ratings.filter((rating)=>{
					return rating.rateType === NEGATIVE;
				});
			}
		}	
	};
</script>

